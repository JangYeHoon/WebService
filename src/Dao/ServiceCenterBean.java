package Dao;

public class ServiceCenterBean {
	private int servicecenterNum;
	private String servicecenterName;
	private String servicecenterTitle;
	private String servicecenterContents;
	private int	servicecenterReNum;
	private int servicecenterReCheck;
	
	public int getServicecenterNum() {
		return servicecenterNum;
	}
	public void setServicecenterNum(int servicecenterNum) {
		this.servicecenterNum = servicecenterNum;
	}
	public String getServicecenterName() {
		return servicecenterName;
	}
	public void setServicecenterName(String servicecenterName) {
		this.servicecenterName = servicecenterName;
	}
	public String getServicecenterTitle() {
		return servicecenterTitle;
	}
	public void setServicecenterTitle(String servicecenterTitle) {
		this.servicecenterTitle = servicecenterTitle;
	}
	public String getServicecenterContents() {
		return servicecenterContents;
	}
	public void setServicecenterContents(String servicecenterContents) {
		this.servicecenterContents = servicecenterContents;
	}
	public int getServicecenterReNum() {
		return servicecenterReNum;
	}
	public void setServicecenterReNum(int servicecenterReNum) {
		this.servicecenterReNum = servicecenterReNum;
	}
	public int getServicecenterReCheck() {
		return servicecenterReCheck;
	}
	public void setServicecenterReCheck(int servicecenterReCheck) {
		this.servicecenterReCheck = servicecenterReCheck;
	}
}
